#include <unistd.h>

void	ft_print_alphabet(void)
{
	int	i;

	i = 'a';
	while (i <= 'z')
	{
		write(1, &i, 1);
		i++;
	}
}

int main()
{
	int	i;

	i = 0;
	while (i < 10)
		i++;
	ft_print_alphabet();
	return (0);

}
